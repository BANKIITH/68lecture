numbers = [4,2,9,1,5,6]
length = len(numbers)
print(f"Lenghth of the lsit: {length}")

total_sum = sum(numbers)
print(f"Lenghth of the lsit: {total_sum}")

max_value = max(numbers)
print(f"Lenghth of the lsit: {max_value}")

min_value = min(numbers)
print(f"Lenghth of the lsit: {min_value}")

sorted_value = sorted(numbers)
print(f"Lenghth of the lsit: {sorted_value}")

bool_list = [False, True, False]
any_true = any(bool_list)
print(f"Is any elements True? {any_true}")

all_true = all(bool_list)
print(f"Is any elements True? {all_true}")

string = "hello"
char_list = list(string)
print(f"list of characters {char_list}")

reversed_numbers = list(reversed(numbers))
print(f"Reversed list: {reversed_numbers}")

enumerate_numbers = list(enumerate(numbers))
print(f"Enumerated list: {enumerate_numbers}")